/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint8_t ec_btn_prev_state = 0;
	Action encoder_offset_action = NoAction;
	int16_t encoder_offset = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  // wait power on
  HAL_Delay(1000);
  load_control_drv->powerControl(1);
  // continue initialization
  MX_DMA_Init();
  MX_DAC_Init();
  MX_TIM2_Init();
  MX_ADC_Init();
  MX_TIM6_Init();
  MX_SPI1_Init();
  MX_TIM21_Init();
  MX_USART1_UART_Init();
  MX_TIM22_Init();
  /* USER CODE BEGIN 2 */
  MX_TIM22_Init();

  // init load control
  load_control_drv->loadInit();

  // init display windows and draw main window
  display_wnd_ctrl->windowsInit();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  if(loadData.is_update_event)
	  {
		  loadData.is_update_event = 0;

		  // check power button state
		  load_control_drv->checkPowerButton();

		  // handle encoder button pressed event
		  if((EC_BTN_GPIO_Port->IDR & EC_BTN_Pin) && ec_btn_prev_state) // button was released
		  {
			    ec_btn_prev_state = 0;
		  }
		  if(!(EC_BTN_GPIO_Port->IDR & EC_BTN_Pin) && !ec_btn_prev_state) // button was pressed
		  {
				ec_btn_prev_state = 1;
				// change window
				display_wnd_ctrl->goToNextWindowOrItem();
		  }

		  // handle encoder offset event
		  encoder_offset = load_control_drv->getEncoderOffset();
		  if(encoder_offset != 0)
		  {
				encoder_offset_action = (encoder_offset > 0) ? Next : Prev;
				display_wnd_ctrl->updateWindowParameters(encoder_offset_action);
		  }

		  // check accumulator voltage in Discharge mode
		  if(loadData.load_settings.load_work_mode == BatteryDischarge && loadData.voltage < loadData.load_settings.discharge_voltage)
		  {
			  // if accumulator voltage lower discharge voltage level, reset current
			  load_control_drv->setEnabled(0);
		  }

		  // update DS18B20 temperature and check over temperature event
		  load_control_drv->updateTemperature();
		  loadData.is_ovt = load_control_drv->checkOvertemperature();
		  if(loadData.is_ovt && loadData.on_state) // if over temperature and load is enabled reset current
		  {
			  load_control_drv->setEnabled(0);
		  }
		  // fan speed control
		  load_control_drv->fanSpeedControl();
	  }

	  // handle end of ADC data conversion event
	  if(loadData.is_conversion_ended)
	  {
		  loadData.is_conversion_ended = 0;
		  // update ADC measured parameters
		  load_control_drv->calcMeasuredParams();
		  // update display data
		  display_wnd_ctrl->refreshWindow();
	  }
  }
  /* USER CODE END 3 */
}

